function fallbackName(name) {
  return name
    .replace(/ Group/g, "グループ")
    .replace(/ Holdings/g, "ホールディングス")
    .replace(/ Corporation/g, "")
    .replace(/ Company/g, "")
    .replace(/ Japan/g, "ジャパン");
}

